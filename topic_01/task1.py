# Method №1
text1 = "..І ти ясна, і я прозорий, І душі наші, мов пісні, І світ великий, неозорий Належить нам — тобі й мені"
reversed_text1 = text1[::-1]
print("\n", reversed_text1)

# Method №2
text2 = "..І ти ясна, і я прозорий, І душі наші, мов пісні, І світ великий, неозорий Належить нам — тобі й мені"
reversed_text2 = ''.join(reversed(text2))
print("\n", reversed_text2)